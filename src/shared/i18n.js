export function getText(settingsOrLanguage = DEFAULT_LANGUAGE) {
  const language = typeof settingsOrLanguage === "string"
    ? settingsOrLanguage
    : settingsOrLanguage?.appLanguage;
  return UI_TEXT[normalizeLanguage(language)];
}

export function applyTranslations(root, text, language = DEFAULT_LANGUAGE) {
  const doc = root.ownerDocument || root;
  doc.documentElement.lang = normalizeLanguage(language) === "en" ? "en" : "zh-CN";

  root.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (text[key]) element.textContent = text[key];
  });

  root.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (text[key]) element.setAttribute("placeholder", text[key]);
  });

  root.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    if (text[key]) element.setAttribute("aria-label", text[key]);
  });
}
